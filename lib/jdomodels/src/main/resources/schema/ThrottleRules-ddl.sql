CREATE TABLE `THROTTLE_RULES`(
    `THROTTLE_ID` bigint(20) unsigned NOT NULL,
    `NORMALIZED_PATH` varchar(255) NOT NULL,
    `MAX_CALLS_PER_USER_PER_SECOND` bigint(20) unsigned NOT NULL,
    `PERIOD_IN_SECONDS` bigint(20) unsigned NOT NULL,
    `MODIFIED_ON` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY(`THROTTLE_ID`),
    UNIQUE (`NORMALIZED_PATH`)
);